package br.com.opet.ex0306_2b;

public class Valor {
	private int valor;
	private int interacao;

	public Valor(int valor, int interacao) {
		this.valor = valor;
		this.interacao = interacao;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	public int getInteracao() {
		return interacao;
	}

	public void setInteracao(int interacao) {
		this.interacao = interacao;
	}
}
